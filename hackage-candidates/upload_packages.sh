FAILED=()

for i in "${!PACKAGES[@]}"; do
    pkg="${PACKAGES[$i]}"
    echo "[$((i + 1))/$TOTAL] Processing: $pkg"
    (
        cd "$pkg"
        cabal sdist
        cabal upload "dist-newstyle/sdist/${pkg}-0.0.0.0.tar.gz" --token "${HACKAGE_TOKEN}"
        curl -X PUT \
             -H "Authorization: X-ApiKey ${HACKAGE_TOKEN}" \
             "https://hackage.haskell.org/package/${pkg}/maintainers/user/IOHK"
    ) && SUCCESS=$((SUCCESS + 1)) || FAILED+=("$pkg")
done

