echo "Done: $SUCCESS/$TOTAL succeeded."

if [ ${#FAILED[@]} -gt 0 ]; then
    echo "Failed packages:"
    for pkg in "${FAILED[@]}"; do
        echo "  - $pkg"
    done
    exit 1
fi
